%T=1
a=0.85;
w=pi/3;
b=3;
fai=pi/4;
z1=a*(cos(w)+1i*sin(w));
z2=a*(cos(w)-1i*sin(w));
r1=b*(cos(fai)+1i*sin(fai));
r2=b*(cos(fai)-1i*sin(fai));
sys1=tf([r1,0],[1,-z1],1);
sys2=tf([r2,0],[1,-z2],1);
sys=parallel(sys1,sys2); 
subplot(211); 
y1=impulse(sys,0:40);
stem(0:40,y1,'.','Markersize',15);
subplot(212)
k=0:40;
y2=2*b*a.^k.*cos(w*k+fai); 
stem(0:40,y2,'.','Markersize',15);